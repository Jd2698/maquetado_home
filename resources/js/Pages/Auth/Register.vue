<script setup>
	import { Head, Link, useForm } from "@inertiajs/vue3";
	import AuthenticationCard from "@/Components/AuthenticationCard.vue";
	import AuthenticationCardLogo from "@/Components/AuthenticationCardLogo.vue";
	import Checkbox from "@/Components/Checkbox.vue";
	import InputError from "@/Components/InputError.vue";
	import InputLabel from "@/Components/InputLabel.vue";
	import PrimaryButton from "@/Components/PrimaryButton.vue";
	import TextInput from "@/Components/TextInput.vue";

	import Icon from "../../Components/Icons/Icon.vue";
	import AppLayout from "@/Layouts/AppHome.vue";

	const form = useForm({
		name: "",
		email: "",
		password: "",
		password_confirmation: "",
		terms: false,
	});

	const submit = () => {
		form.post(route("register"), {
			onFinish: () => form.reset("password", "password_confirmation"),
		});
	};
</script>

<template>

	<AppLayout title="Register">
		<AuthenticationCard>
			<template #logo>
				<!-- <Icon width="50" /> -->
			</template>

			<form @submit.prevent="submit">
				<div>
					<InputLabel for="name" value="Name" />
					<TextInput id="name" v-model="form.name" type="text" class="mt-1" required autofocus autocomplete="name" />
					<InputError class="mt-2" :message="form.errors.name" />
				</div>

				<div class="mt-4">
					<InputLabel for="email" value="Email" />
					<TextInput id="email" v-model="form.email" type="email" class="mt-1" required autocomplete="username" />
					<InputError class="mt-2" :message="form.errors.email" />
				</div>

				<div class="mt-4">
					<InputLabel for="password" value="Password" />
					<TextInput id="password" v-model="form.password" type="password" class="mt-1" required autocomplete="new-password" />
					<InputError class="mt-2" :message="form.errors.password" />
				</div>

				<div class="mt-4">
					<InputLabel for="password_confirmation" value="Confirm Password" />
					<TextInput id="password_confirmation" v-model="form.password_confirmation" type="password" class="mt-1" required autocomplete="new-password" />
					<InputError class="mt-2" :message="form.errors.password_confirmation" />
				</div>

				<div class="d-flex justify-content-between pt-4 mt-4">
					<Link :href="route('login')" class="" style="text-decoration: none;">
					Already registered?
					</Link>

					<PrimaryButton class="ms-4" :disabled="form.processing">
						Register
					</PrimaryButton>
				</div>
			</form>
		</AuthenticationCard>
	</AppLayout>

</template>
